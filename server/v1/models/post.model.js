const mongoose = require("mongoose");

const postSchema = new mongoose.Schema({
  title: String,
  price: Number,
  negotiable: Boolean,
  description: String,
  postingDate: { type: Date, default: Date.now() },
  seenBy: String,
  currentLocation: String,
  status: String,
  type: String,
  brand: String,
  model: String,
  mYear: String,
  km: Number,
  mileage: String,
  noOfOwner: Number,
  fuelType: String,
  rtoNo: String,
  chassisNo: String,
  color: String,
  insurance: { expiry: Date, type: String },
  transmissionType: String,
  history: String,
  lastService: Date,
  airbags: Number,
  musicSystem: String,
  orvm: String,
  engineStartStop: String,
  centralLocking: String,
  sunroof: String,
  powerWindow: String,
  headLamps: String,
  ac: String,
  loanStatus: String,
  engineType: String,
  driveTrain: String,
  steeringType: String,
  bhp: String,
  fueltank: Number,
  seatingCapacity: Number,
  bootSpace: Number,
  length: Number,
  height: Number,
  groundClearance: Number,
  interiorImages: { type: Array },
  exteriorImages: { type: Array },
  rtoLicenseType: String,
});

module.exports = mongoose.model("Post", postSchema);
